class Reading {
  public String timestamp;
  public double temperature;
  public double humidity;
  public double  co2;

  public void setTimestamp(String ts) {
    this.timestamp = ts;
  }
  public void setTemperature(double ts) {
    this.temperature = ts;
  }
  public void setHumidity(double ts) {
    this.humidity = ts;
  }
  public void setCO2(double ts) {
    this.co2 = ts;
  }
}
